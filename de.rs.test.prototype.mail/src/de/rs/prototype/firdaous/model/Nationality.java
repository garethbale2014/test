package de.rs.prototype.firdaous.model;

public class Nationality {
	
	private String name;
	private String internationalAbrevation;
	
	
	public Nationality() {
		
	}
	
	public String getInternationalAbrevation() {
		return internationalAbrevation;
	}
	
	public void setInternationalAbrevation(String internationalAbrevation) {
		this.internationalAbrevation = internationalAbrevation;
	}
	
	public String getName() {
		return name;
	}
	
	public void setName(String name) {
		this.name = name;
	}

}
